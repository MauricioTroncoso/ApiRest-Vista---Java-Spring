
package MODEL;


public class categoria {
    String nombre;
    int id;

    public categoria() {
    }

    public categoria(String name, int id) {
        this.nombre = name;
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

   

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    
   
    
    
}
